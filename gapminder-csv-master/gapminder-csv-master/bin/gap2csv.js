function ready(error, results) {
  if (error) throw error;

  var tables = results.map((d) => csvParse(d));

	tables.forEach(function(table) {
		var cols = keys(table[0]);
		var ind = cols[2];
		columns.push(ind);
		table.forEach(function(d) {
			if (!(d.geo in lookup)) { lookup[d.geo] = {} }
			if (!(d.time in lookup[d.geo])) { lookup[d.geo][d.time] = {} }
			lookup[d.geo][d.time][ind] = +d[ind];
		});
	});

	for (var country in lookup) {
		for (var year in lookup[country]) {
			var obj = lookup[country][year]
			obj.geo = country;
			obj.time = year;
			data.push(obj);
		}
	}

  var output_csv = csvFormat(data, columns);

  fs.writeFile("gapminder.csv", output_csv, function(err) {
    if(err) {
      return console.log(err);
    }

    console.log("The file was saved!");
  });

}

